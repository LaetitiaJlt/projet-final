				<p>1. Préchauffez le four à 100°. Lavez l'orange et séchez-la. A l'aide d'un économe, retirez le zeste en prenant bien soin de ne pas prélever la partie blanche trop amère. Taillez le zeste en fines lamelles puis en tout petits dés. Disposez ce zeste
					sur une plaque de four en étalant bien afin que les morceaux ne se superposent pas. Laissez sécher 1h au four.</p>
