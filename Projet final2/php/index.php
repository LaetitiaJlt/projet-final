				<h2>Le thé en vrac ou en sachet</h2>
				<img src="images/vrac.jpg" alt="photographie d'une théière avec plusieur tasses contenant différents thé" >
				<p>Le thé en vrac permet d’adapter la quantité de thé au volume d’eau choisi en toute simplicité. Tasse, mug, théière individuelle ou de grande taille</p>
				<p>C’est une évidence, les thés rares et haut de gamme délivrent avec plus de complexité tous leurs arômes lorsqu’ils sont proposés en feuilles entières. Il en est de même pour des thés plus classiques, parfumés ou aromatisés, qu’ils soient noirs, verts,
					blancs ou wu long.</p>
				<p>Les feuilles de thé doivent également être chauffées à bonne température pour exprimer toute la richesse d’un thé. Fragiles, les thés verts infusent autour de 70 à 80 °C contre 90 °C pour un thé noir. Si vous ne possédez pas de bouilloire avec thermostat, une astuce consiste à porter de l’eau à ébullition puis à y ajouter un quart d’eau froide avant d’y infuser son thé. Le temps d’infusion est généralement de 1 à 3 minutes pour un thé du Japon et 2 à 4 minutes pour un thé de Chine.</p>
				<img src="images/image.jpg" alt="Une tasse transparente avec un sachet de thé" class="droite">
				<p>Pour la petite histoire : Cette façon de vendre le thé n’est pas récente. Contrairement à toute attente on ne la doit pas aux amoureux du thé que sont les anglais. Mais à un américain !Le thé en sachet date de 1908. Et même d’un peu plus tôt puisque
					5 ans avant, aux Etats Unis, fut déposé le premier brevet de pochettes à thé en soie.</p>
				<p>Désormais, le sachet de thé / ou infusette est devenu l’un des conditionnements les plus utilisés au monde, 75 % de la consommation de thé est en sachet. Rapide à préparer, la dose est déjà prête, c’est ce qui a contribué à sa croissance dans une
					société où le « vite fait – bien fait » prime. Il est également plus propre, il suffit de jeter le sachet après utilisation.</p>
				<p>Le thé vert perd vite son arôme, car son oxydation est très rapide. En sachet, il se préserve donc mieux en emballage individuel. Qu’il soit en vrac ou ensaché, de manière générale il se conserve à l’abri de la lumière et si possible au frais mais sans humidité. Si vous le placez au frigo, placez-le dans une boîte hermétique afin qu’il ne s’imprègne pas des odeurs fortes (oignons, fromages…).</p>
